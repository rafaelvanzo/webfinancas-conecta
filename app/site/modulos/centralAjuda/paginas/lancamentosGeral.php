                 <h5>COMO FUNCIONA A TABELA DE LANÇAMENTOS </h5>  
  
                  <div align="center"> <img class="img-responsive" src="site/img/centralAjuda/lancamentos/lancamentos_geral04.png"> </div> 
                   
                   <br />                 
                  
                 <p style="line-height:30px;"> 
                  <b class="legendaNumero">1.</b> Para localizar um lançamento específico dentro da conta financeira digite a <b>DESCRIÇÃO</b>, <b>DATA DE COMPENSAÇÃO</b> ou <b>VALOR</b> para localizar um ou mais lançamentos. <br />
                  <b class="legendaNumero">2.</b> Utilize a opção <b>MOSTRAR</b> para visualizar uma quantidade maior de lançamentos por página. <br />
