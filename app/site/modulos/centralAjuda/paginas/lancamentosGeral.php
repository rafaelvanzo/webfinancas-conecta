                  <b class="legendaNumero">5.</b> As cores <b>verde</b> e <b>vermelho</b> dos valores definem se o lançamento é um <b>CRÉDITO</b> ou um <b>DÉBITO</b> na conta financeira, sendo verde para <b>CRÉDITO</b> e vermelha para <b>DÉBITO</b>. <br />
