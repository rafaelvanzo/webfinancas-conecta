									<h5>NOVO RECEBIMENTO</h5>
                                    
                      <p>Todos os lançamentos registrados na aba <b>LANÇAMENTOS</b> serão reconhecidos pelo sistema como uma operação financeira já concluída. </p>
                      <p>Todos os lançamentos de <b>RECEBIMETNOS</b> e <b>PAGAMENTOS</b> poderão ser vinculados a um ou mais <b>PLANO DE CONTAS (CATEGORIAS)</b> ou <b>CENTRO DE RESPONSABILIDADE</b>.</p>              
                       
                                     
                       <div align="center"> <img class="img-responsive" src="site/img/centralAjuda/lancamentos/lancamentosPreencherRecebimentos.png"> </div> 
                   
                   <br />
                   
                  <p style="line-height:30px;"> 
                  <b class="legendaNumero">1.</b> Preencha uma descrição sucinta do lançamento para facilitar a identificação. <br />
                  <b class="legendaNumero">2.</b> Digite o nome do <b>FAVORECIDO</b> para o sistema listar todos os favorecidos cadastros, caso o <b>FAVORECIDO</b> não esteje cadastrado, clique na opção que aparecerá a palavra <b>( Adicionar )</b> ao lado para adicioná-lo. <br />
                  <b class="legendaNumero">3.</b> Digite o nome da <b>CONTA FINANCEIRA</b> previamente cadastrada, quando estiver digitatando o sistema começará a mostrar as <b>CONTA FINANCEIRA</b> cadastradas. Clique no nome da <b>CONTA FINANCEIRA</b> para selecionar. <br />
                  <b class="legendaNumero">4.</b> Preencha a data na qual você enviou o faturamento para o favorecido. <br />
                  <b class="legendaNumero">5.</b> Selecione na lista de documentos qual foi o tipo de documento enviado para o favorecido. <br />
                  <b class="legendaNumero">6.</b> Preencha a data de vencimento do lançamento. <br />
                  <b class="legendaNumero">7.</b> Preencha a data de compensação do lançamento.  Essa data informará para o sistema quando que efetivamente o dinheiro estará <b>DISPONÍVEL</b> na conta financeira.  <br />
                  <b class="legendaNumero">8.</b> Preencha o valor do lançamento.<br />
                  <b class="legendaNumero">9.</b> Preencha a forma de pagamento. <br />
                  <b class="legendaNumero">10.</b> Observações e informações extras sobre o lançamento. <br />
									</p>                   
                 
                 <hr class="tall">
                 
                       <div align="center"> <img class="img-responsive" src="site/img/centralAjuda/lancamentos/lancamentos_quitar_cancelar.png"> </div> 
                   
                   <br />
                   
                  <p style="line-height:30px;"> 
                  <b class="legendaNumero">1.</b> Para finalizar o lançamento clique no botão <b>QUITAR</b>. <br />
                  <b class="legendaNumero">2.</b> Para cancelar o lançamento clique no botão <b>CANCELAR</b>. <br />
									</p>
                 
                 <hr class="tall">
                      
                 <h5>ADICIONANDO VALORES EM UM PLANO DE CONTAS (CATEGORIAS)/ CENTRO DE RESPONSABILIDADE</h5>
                                    
                      <p>Só será possível adicionar valores a um PLANO DE CONTAS ou a um CENTRO DE RESPONSABILIDADE ao incluir um lançamento (<b>RECEBIMENTO</b> ou <b>PAGAMENTO</b>).</p>              
                                    
                       <div align="center"> <img class="img-responsive" src="site/img/centralAjuda/lancamentos/lancamentos_plano_contas.png"> </div>   
                  
                  <br />
                   
                  <p style="line-height:30px;"> 
                  <b class="legendaNumero">1.</b> Clique na aba <b>PLANO DE CONTAS / CENTRO DE RESPONSABILIDADE</b> para abrir o formulário. <br />
									</p>      
                
                <hr class="tall"> 
                
                <p style="line-height:30px;"> 
                  Você poderá adicionar valores dentro de um <b>PLANO DE CONTAS (CATEGORIA)</b> ou <b>CENTRO DE RESPONSABILIDADE</b> ou até mesmo aos dois ao mesmo tempo. <br />
                  Você poderá adicionar valores a quantos <b>PC</b> e <b>CR</b> quiser, desde de que a soma dos valores seja menor ou igual ao valor do lançamento.<br /><br />
                  </p>
                
                       <div align="center"> <img class="img-responsive" src="site/img/centralAjuda/lancamentos/lancamentos_novo_pc_cr.png"> </div> 
                   
                   <br />
                   
                 <p style="line-height:30px;"> 
                  <b class="legendaNumero">1.</b> Para adicionar um <b>PLANO DE CONTAS (CATEGORIA)</b> digite o nome do <b>PLANO DE CONTAS</b> já cadastrado para que o sistema localize, após localiza-lo clique para selecionar. <br />
                  <b class="legendaNumero">2.</b> Para adicionar um <b>CENTRO DE RESPONSABILIDADE</b> digite o nome do <b>CENTRO DE RESPONSABILIDADE</b> já cadastrado para que o sistema localize, após localiza-lo clique para selecionar. <br />
                  <b class="legendaNumero">3.</b> Digite o valor, ou a porcentagem para adicionar um <b>PLANO DE CONTAS / CENTRO DE RESPONSABILIDADE</b>. <br />
                  <b class="legendaNumero">4.</b> Clique no botão <img src="site/img/centralAjuda/bt_ok.png" /> para adicionar os valores aos <b>PLANO DE CONTAS (CATEGORIA)</b> e <b>CENTRO DE RESPONSABILIDADE</b>. <br />
                  <b class="legendaNumero">5.</b> Após a adicionar o <b>PLANO DE CONTAS / CENTRO DE RESPONSABILIDADE</b> eles serão exibidos da seguinte forma <b>PLANO DE CONTAS (CATEGORIA)</b> como <b>CR</b> e <b>CENTRO DE RESPONSABILIDADE</b> como <b>PC</b>.  <br />
                  <b class="legendaNumero">6.</b> Demonstra o valor adicionado para o <b>PC/CR</b> e o seu percentual correspondente ao valor total do lançamento. <br />
                  <b class="legendaNumero">7.</b> Para remover os valores adicionados ao <b>PC/CR</b> clique no botão <img src="site/img/centralAjuda/bt_excluir.png" />. <br />
                  <b class="legendaNumero">8.</b> Para retornar ao formulário com os dados do lançamento clique em <b>DADOS DO LANÇAMENTO</b>. <br />
                  <b class="legendaNumero">9.</b> Para finalizar o lançamento clique no botão <b>QUITAR</b>. <br />
                  <b class="legendaNumero">10.</b> Para cancelar o lançamento clique em <b>CANCELAR</b>. <br />
									</p>       
                           
