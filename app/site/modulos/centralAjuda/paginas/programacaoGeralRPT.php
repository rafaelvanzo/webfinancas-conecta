                 <h5>COMO FUNCIONA A TABELA DE PROGRAMAÇÃO </h5>  
  
                  <div align="center"> <img class="img-responsive" src="site/img/centralAjuda/programacao/programacao_tabela01.png"> </div> 
                   
                   <br />                 
                  
                 <p style="line-height:30px;"> 
                  <b class="legendaNumero">1.</b> Para localizar um lançamento programado específico dentro da conta financeira digite a <b>DESCRIÇÃO</b>, <b>DATA DE COMPENSAÇÃO</b> ou <b>VALOR</b> para localizar um ou mais lançamentos. <br />
                  <b class="legendaNumero">2.</b> Utilize a opção <b>MOSTRAR</b> para visualizar uma quantidade maior de lançamentos por página. <br />
                  <b class="legendaNumero">3.</b> Data de vencimento do lançamento programado. <br />
                  <b class="legendaNumero">4.</b> Quando um lançamento programado, seja ele um <b>RECEBIMENTO</b>, <b>PAGAMENTO</b> ou <b>TRANSFERÊNCIA</b>, estiver atrasado o sistema irá mostrar a quantidade de dias em atraso. <br />
                  <b class="legendaNumero">5.</b> Descrição do lançamento. <br />
                  <b class="legendaNumero">6.</b> Mostrar em qual <b>CONTA FINANCEIRA</b> o lançamento programado esta associado. <br />
                  <b class="legendaNumero">7.</b> As cores <b>verde</b> e <b>vermelho</b> dos valores definem se o lançamento é um <b>CRÉDITO</b> ou um <b>DÉBITO</b> na conta financeira, sendo verde para <b>CRÉDITO</b> e vermelha para <b>DÉBITO</b>. <br />
                  <b class="legendaNumero">8.</b> Clique no botão <img src="site/img/centralAjuda/bt_editar.png"> para abrir a janela de edição ( <a href="centralAjuda/programacaoEditarLancamentos">Editar Recebimentos Programados, Pagamentos Programados e Transferências Programados</a> ).  <br />
                  <b class="legendaNumero">9.</b> Clique no botão <img src="site/img/centralAjuda/bt_excluir.png"> para excluir o lançamento correspondente. <br />
								</p>   
                
