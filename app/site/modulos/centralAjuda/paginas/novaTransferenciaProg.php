									<h5>NOVA TRANSFERÊNCIA PROGRAMADA</h5>
                                    
                      <p>Essa ferramenta não serve para lançar transferência para <b>TERCEIROS</b>. Foi uma ferramenta desenvolvida para transferir valores de uma conta financeira para outra. <br />
                      <b>Exemplo:</b> Cliente Web Finanças precisa transferir R$ 100,00 da conta financeira <b>LIVRO DE CAIXA</b> para a conta financeira <b>BANCO DO BRASIL</b> dele mesmo (Cliente do Web Finanças). </p>              
                   
                    <div align="center"> <img class="img-responsive" src="site/img/centralAjuda/programacao/programacaoTransferencia01.png"> </div> 
                   
                   <br />
                   
                  <p style="line-height:30px;"> 
                  <b class="legendaNumero">1.</b> Preencha uma breve descrição do lançamento para fácilitar a identificação.. <br />
                  <b class="legendaNumero">2.</b> Digite o nome da <b>CONTA FINANCEIRA DE ORIGEM</b> previamente cadastrada, quando estiver digitatando o sistema começará a mostrar as <b>CONTA FINANCEIRA DE ORIGEM</b> cadastradas. Clique no nome da <b>CONTA FINANCEIRA DE ORIGEM</b> para selecionar e informar ao sistema de qual conta será feito o <b>DÉBITO</b>. <br />
                  <b class="legendaNumero">3.</b> Digite o nome da <b>CONTA FINANCEIRA DE DESTINO</b> previamente cadastrada, quando estiver digitatando o sistema começará a mostrar as <b>CONTA FINANCEIRA DE DESTINO</b> cadastradas. Clique no nome da <b>CONTA FINANCEIRA DE DESTINO</b> para selecionar e informar ao sistema em qual conta será feito o <b>CRÉDITO</b>. <br />
                  <b class="legendaNumero">4.</b> Preencha qual foi a data de cadastro da transferência no sistema. <br />
                  <b class="legendaNumero">5.</b> Preencha a data de vencimento da transferência. <br />
                  <b class="legendaNumero">6.</b> Selecione a opção <b>AUTOMÁTICAMENTE</b> para que o sistema registre o lançamento como efetivado automáticamente no seu vencimento ou a opção <b>MANUAL</b> para que o usuário registre manualmente. <br />
                  <b class="legendaNumero">7.</b> Preencha o valor da transferência. <br />
                  <b class="legendaNumero">8.</b> Observações e informações extras sobre o lançamento. <br />
									</p>
                 
