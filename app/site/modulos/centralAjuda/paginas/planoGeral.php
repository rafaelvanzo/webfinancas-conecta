                 <h5>COMO FUNCIONA A TABELA DO PLANO DE CONTAS</h5>  
  
                  <div align="center"> <img class="img-responsive" src="site/img/centralAjuda/planoContas/plano_tabela.png"> </div> 
                   
                   <br />                 
                  
                 <p style="line-height:30px;"> 
                  <b class="legendaNumero">1.</b> Localize um <b>PLANO DE CONTAS</b> digitando o nome para o sistema localizar. <br />
                  <b class="legendaNumero">2.</b> Utilize a opção <b>MOSTRAR</b> para visualizar uma quantidade maior de registros por página. <br />
                  <b class="legendaNumero">3.</b> Na coluna <b>PLANO DE CONTAS</b> você poderá visuallizar o nome e o número dos <b>PLANO DE CONTAS</b>. <br />
                  <b class="legendaNumero">4.</b> Na coluna <b>VALOR</b> você poderá visuallizar todos os valores agrupados no <b>PLANO DE CONTAS</b>. <br />
                  <b class="legendaNumero">5.</b> Na coluna <b>CÓDIGO DE REF.</b> você poderá visuallizar o código de referencia do <b>PLANO DE CONTAS</b>. <br />
                  <b class="legendaNumero">6.</b> Na coluna <b>TIPO</b> você poderá visuallizar se o <b>PLANO DE CONTAS</b> é sintético ou análitico. <br />
                   <b>CENTRO SINTÉTICOS</b> são aqueles centros que somente agrupam outros plano de cotnas.<br />
                   <b>CENTRO ANALITICOS</b> são aqueles centros que agrupam somente valores, mas não podem agrupar outros centros.<br />
                  
                  <b class="legendaNumero">7.</b> Na coluna <b>SITUAÇÃO</b> informa se o centro de responsabilidade está <b>ATIVO</b> ou <b>INATIVO</b>. <br />
                  <b class="legendaNumero">8.</b> Clique no botão <img src="site/img/centralAjuda/bt_editar.png"> para <b>EDITAR</b> um centro. <br />
                  <b class="legendaNumero">9.</b> Clique no botão <img src="site/img/centralAjuda/bt_excluir.png"> para <b>EXCLUÍR</b> um centro. <br />
								</p>   
                
