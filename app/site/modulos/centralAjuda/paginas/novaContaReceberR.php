									<h5>NOVO RECEBIMENTO RECORRENTE</h5>
                                    
                      <p>Todos os lançamentos registrados na aba <b>PROGRAMAÇÃO</b> serão reconhecidos pelo sistema como uma <b>PROGRAMAÇÃO</b> de uma operação financeira, seja um <b>RECEBIMENTO</b>, <b>PAGAMENTO</b> ou uma <b>TRANSFERÊNCIA</b>. </p>
                      <p>Todos os lançamentos de <b>RECEBIMETNOS</b> e <b>PAGAMENTOS</b> poderão ser vinculados a um ou mais <b>PLANO DE CONTAS (CATEGORIAS)</b> ou <b>CENTRO DE RESPONSABILIDADE</b> veja como mais abaixo.</p>              
                                     
                       <div align="center"> <img class="img-responsive" src="site/img/centralAjuda/programacao/programacaoRecebimentoRecorrente01.png"> </div> 
                   
                   <br />
                   
                  <p style="line-height:30px;"> 
                  <b class="legendaNumero">1.</b> Preencha uma breve descrição do lançamento para fácilitar a identificação. <br />
                  <b class="legendaNumero">2.</b> Digite o nome do <b>FAVORECIDO</b> para o sistema listar todos os favorecidos cadastros, caso o <b>FAVORECIDO</b> não esteje cadastrado, clique na opção que aparecerá a palavra <b>( Adicionar )</b> ao lado para adicioná-lo. <br />
                  <b class="legendaNumero">3.</b> Digite o nome da <b>CONTA FINANCEIRA</b> previamente cadastrada, quando estiver digitatando o sistema começará a mostrar as <b>CONTA FINANCEIRA</b> cadastradas. Clique no nome da <b>CONTA FINANCEIRA</b> para selecionar e associar o lançamento. <br />
                  <b class="legendaNumero">4.</b> Selecione na lista de documentos qual foi o tipo de documento. <br />
                  <b class="legendaNumero">5.</b> Selecione a forma de pagamento. <br />
                  <b class="legendaNumero">8.</b> Selecione a opção <b>AUTOMÁTICAMENTE</b> para que o sistema registre o lançamento como efetivado automáticamente no seu vencimento ou a opção <b>MANUAL</b> para que o usuário registre manualmente.<br />
                  <b class="legendaNumero">7.</b> Configure se o sistema deverá considerar os <b>SÁBADOS E DOMINGOS</b> em um <b>RECEBIMENTO</b>, <b>PAGAMENTO</b> ou <b>TRANSFERÊNCIA</b>, caso o vencimento seja no final de semana.  <br />
                  <b class="legendaNumero">8.</b> Preencha o valor do lançamento.<br />
                  <b class="legendaNumero">9.</b> Preencha qual será o intervalo entre os vencimentos das faturas desse lançamento para que o sistema gere automáticamente os demais lançamentos. <br />
                  <b class="legendaNumero">10.</b> Preencha o dia que esse lançamento sempre estará vencendo. <br />
                  <b class="legendaNumero">11.</b> Preencha em qual data iniciará o lançamento recorrente. <br />
                  <b class="legendaNumero">12.</b> Preencha qualquer informação sobre o lançamento. <br />
									</p>                   
                 
                 <hr class="tall">
                 
                       <div align="center"> <img class="img-responsive" src="site/img/centralAjuda/bt_salvar_cancelar.png"> </div> 
                   
                   <br />
                   
                  <p style="line-height:30px;"> 
                  <b class="legendaNumero">1.</b> Para finalizar o lançamento clique no botão <b>SALVAR</b>. <br />
                  <b class="legendaNumero">2.</b> Para cancelar o lançamento clique no botão <b>CANCELAR</b>. <br />
									</p>
                 
                 <hr class="tall">
                      
                 <h5>ADICIONANDO VALORES EM UM PLANO DE CONTAS (CATEGORIAS)/ CENTRO DE RESPONSABILIDADE</h5>
                                    
                      <p>Os valores adicinoados a um PLANO DE CONTAS ou a um CENTRO DE RESPONSABILIDADE só poderão ser efetuado ao incluir um lançamento, seja ele um <b>RECEBIMENTO</b> ou <b>PAGAMENTO</b>.</p>              
                                    
                       <div align="center"> <img class="img-responsive" src="site/img/centralAjuda/programacao/lancamentos_plano_contas.png"> </div>   
                  
                  <br />
                   
                  <p style="line-height:30px;"> 
                  <b class="legendaNumero">1.</b> Clique na aba <b>PLANO DE CONTAS / CENTRO DE RESPONSABILIDADE</b> para abrir o formulário. <br />
									</p>      
                
                <hr class="tall"> 
                
                 <p style="line-height:30px;"> 
                  Você poderá adicionar valores dentro de um <b>PLANO DE CONTAS (CATEGORIA)</b> ou <b>CENTRO DE RESPONSABILIDADE</b> ou até mesmo aos dois ao mesmo tempo. <br />
                  Você poderá adicionar valores a quantos <b>PC</b> e <b>CR</b> quiser, desde de que a soma dos valores seja menor ou igual ao valor do lançamento.<br /><br />
                  </p>
                
                       <div align="center"> <img class="img-responsive" src="site/img/centralAjuda/programacao/lancamentos_novo_pc_cr.png"> </div> 
                   
                   <br />
                   
                 <p style="line-height:30px;"> 
                  <b class="legendaNumero">1.</b> Para adicionar um <b>PLANO DE CONTAS (CATEGORIA)</b> digite o nome do <b>PLANO DE CONTAS</b> já cadastrado para que o sistema localize, após localiza-lo clique para selecionar. <br />
                  <b class="legendaNumero">2.</b> Para adicionar um <b>CENTRO DE RESPONSABILIDADE</b> digite o nome do <b>CENTRO DE RESPONSABILIDADE</b> já cadastrado para que o sistema localize, após localiza-lo clique para selecionar. <br />
                  <b class="legendaNumero">3.</b> Digite o valor, ou a porcentagem para adicionar um <b>PLANO DE CONTAS / CENTRO DE RESPONSABILIDADE</b>. <br />
                  <b class="legendaNumero">4.</b> Clique no botão <img src="site/img/centralAjuda/bt_ok.png" /> para adicionar os valores aos <b>PLANO DE CONTAS (CATEGORIA)</b> e <b>CENTRO DE RESPONSABILIDADE</b>. <br />
                  <b class="legendaNumero">5.</b> Após a adicionar o <b>PLANO DE CONTAS / CENTRO DE RESPONSABILIDADE</b> eles serão exibidos da seguinte forma <b>PLANO DE CONTAS (CATEGORIA)</b> como <b>CR</b> e <b>CENTRO DE RESPONSABILIDADE</b> como <b>PC</b>.  <br />
                  <b class="legendaNumero">6.</b> Demonstra o valor adicionado para o <b>PC/CR</b> e o seu percentual correspondente ao valor total do lançamento. <br />
                  <b class="legendaNumero">7.</b> Para remover os valores adicionados ao <b>PC/CR</b> clique no botão <img src="site/img/centralAjuda/bt_excluir.png" />. <br />
                  <b class="legendaNumero">8.</b> Para retornar ao formulário com os dados do lançamento clique em <b>DADOS DO LANÇAMENTO</b>. <br />
                  <b class="legendaNumero">9.</b> Para finalizar o lançamento clique no botão <b>SALVAR</b>. <br />
                  <b class="legendaNumero">10.</b> Para cancelar o lançamento clique em <b>CANCELAR</b>. <br />
									</p>     
                           
